package kr.or.ddit.prod.model;

public class Prod {
	private String prodGnm;
	private String prodGu;
	private String buyerName;
	private String prodId;
	private String prodNm;
	private String price;
	
	public String getProdGnm() {
		return prodGnm;
	}
	public void setProdGnm(String prodGnm) {
		this.prodGnm = prodGnm;
	}
	public String getProdGu() {
		return prodGu;
	}
	public void setProdGu(String prodGu) {
		this.prodGu = prodGu;
	}
	public String getBuyerName() {
		return buyerName;
	}
	public void setBuyerName(String buyerName) {
		this.buyerName = buyerName;
	}
	public String getProdId() {
		return prodId;
	}
	public void setProdId(String prodId) {
		this.prodId = prodId;
	}
	public String getProdNm() {
		return prodNm;
	}
	public void setProdNm(String prodNm) {
		this.prodNm = prodNm;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
	
	
	
	

}
